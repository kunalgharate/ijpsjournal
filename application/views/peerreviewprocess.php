                            <h2>Peer Review Process </h2>                                      
                        
                        
                            <h4>Step 1 </h4>
                            <p class="middle">Author: Author Submits his/her research work to IJPS by online submission on our website ( www.ijpsjournal.com ) or by mail editor@ijpsjournal.com )</p>                                         
                      

                            <h4>Step 2</h4>
                            <p class="middle">Editor: All the articles are tested for format and checked for grammatical and formatting mistakes. Not more than 2 mistakes per page are allowed. All papers of the journal are double-blind reviewed by at least two reviewers, and ideally is double-blind reviewed by three or more reviewers.</p>                                         
                     

                            <h4>Step 3</h4>
                            <p class="middle">Reviewer: Subject Experts will check your work & give the status:</p>
                            <p>1. Accepted</p> 
                            <p>2. Rejected</p> 
                            <p>3. Accepted with Minor Changes</p> 
                            <p>4. Accepted with Major Changes</p>   
                                                                                     
                        

                            <h4>Step 4</h4>
                            <p class="middle">Author: If Manuscript is accepted then author have to send the Complete Manuscript, Online Maintenance Charges & Copyright form. The entire format can be downloaded from our website www.ijpsjournal.com </p>                                         
                        

                            <h4>Step 5</h4>
                            <p class="middle">Editor: After completion of above mentioned process editor will send manuscript to publisher.</p>                                         
                    

                            <h4>Step 6</h4>
                            <p class="middle">Publisher: Publisher will publish the manuscript in current issue.</p>                                         
                        
                            <h4>Step 7 </h4>
                            <p class="middle">Editor: Editor will inform to Author via E-Mail</p>                                         
                      

