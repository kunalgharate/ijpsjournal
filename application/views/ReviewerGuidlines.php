<h4>Why review?</h4>
<p class="middle">Manuscripts of potential interest and novel research to our readership are sent for formal review, generally to three reviewers, but sometimes more if special guidance is needed (for example on statistics or a particular technique).
<br><br>
By acting as a reviewer you can:<br></p>

<p class="middle"><b> &#9679;   Help authors improve their papers by providing your professional expertise.</b>&nbsp;Gain a sense of prestige in being consulted as an expert!<br><b> &#9679; 
Play an important role in maintaining a good, rigorous peer-review process.</b><br><b> &#9679; Expand your awareness of the current research emerging within your field.</b><br><b> &#9679; Build relationships and improve your academic and professional profile</b>.Although often anonymous, the review process can enable a discussion (between author, reviewer, and editor) around a research field or topic.<br><b>&#9679;Improve your own writing skills.</b>Reviewing others work can make it easier to spot commons errors in your own.<br></p>
<h4>Before saying “yes”</h4>
<h5>Before agreeing to review for a manuscript in IJPS journal, consider the following:</h5>
<p class="middle">&#9679;You need to register in IJPS Journal on home page by registering yourself as reviewer, and create your login ID.
    <br>&#9679; IJPS Journal operating Double blind form of review.  <br>&#9679;
You will need to submit your review online as there a structured form for reviewers to complete. <br>&#9679;Do you have any conflicts of interest? If so, make the editor aware immediately.<br>&#9679;Whether you can complete the review in the allotted time. If you later find yourself struggling to meet the deadline, let the editor know, so they can inform the author of any delays & can get it reviewed from other additional reviewers on time. </p>
<h4>Writing a review: a step-by-step guide</h4>
<p class="middle">You’ve received your invite and said yes, here’s what to do next.</p>
<h5>1. Research the journal</h5>
<p class="middle">&#9679;
    Visit the journal homepage to get a sense of the journal’s published content and house style. This will help you in deciding whether the paper being reviewed is suitable for the journal or not.
   <br>&#9679; Refer to the Instructions for Authors to check if the paper meets the submission criteria of the journal (e.g. length, scope, and presentation).
</p>
<h5>2. Write your report</h5>
<p class="middle">&#9679;Write your views about the manuscript online on reviewer’s panel.</p>
<h4>Questions to consider</h4>
<p class="middle">The main factors you should provide advice on as a reviewer are the originality, presentation, relevance, and significance of the manuscript’s subject matter to the readership of the journal.</p>
<h5>Questions to have in mind when reading the manuscript (in no particular order):</h5>

<p class="middle">
&#9679;Is the submission original?<br>
&#9679; Does the paper fit the scope of the journal?<br>
&#9679;  Would the paper be of interest to the readership of the journal?<br>
&#9679;  Does the paper help to expand or further research in this subject area?<br>
&#9679; Does it significantly build on (the author’s) previous work?<br>
&#9679;  Do you feel that the significance and potential impact of a paper is high or low?<br>
&#9679;   Is the paper complete? Is there an abstract or summary of the work undertaken as well as a concluding section?<br>
&#9679;   Is the methodology presented in the manuscript and any analysis provided both accurate and properly conducted?<br>
&#9679;   Are all relevant accompanying data, citations, or references given by the author?<br>
&#9679;   Should it be shortened and reconsidered in another form?
&#9679;   Would you recommend that the author reconsider the paper for a related or alternative journal?<br>
&#9679;   Is the submission in Standard English to aid the understanding of the reader?<br>
</p>
<h5>Provide detailed comments</h5>
<p class="middle">
&#9679;  These should be suitable for transmission to the authors: use the comment to the author as an opportunity to seek clarification on any unclear points and for further elaboration.<br>
&#9679;   If you have time, make suggestions as to how the author can improve clarity, succinctness, and the overall quality of presentation.<br>
&#9679; Confirm whether you feel the subject of the paper is sufficiently interesting to justify its length; if you recommend shortening, it is useful to the author(s) if you can indicate specific areas where you think that shortening is required.<br>
&#9679;    It is not the job of the reviewer to edit the paper for English, but it is helpful if you correct the English where the technical meaning is unclear.<br>
&#9679;   A referee may disagree with the author’s opinions, but should allow them to stand, provided they are consistent with the available evidence.<br>
&#9679;   Remember that authors will welcome positive feedback as well as constructive criticism from you.<br>

<br>Being critical whilst remaining sensitive to the author isn’t always easy and comments should be carefully constructed so that the author fully understands what actions they need to take to improve their paper. For example, generalized or vague statements should be avoided along with any negative comments which aren’t relevant or constructive.<br></p>
<h5>Process of assessment of manuscript</h5>
<p class="middle"><br>
Reviewers are asked to provide an assessment of the various aspects of a manuscript:<br>
<br>
&#9679;      Key Findings: Please summarize what you consider to be the outstanding features of the work.<br>
&#9679;      Originality and significance: If the conclusions are not original, please provide relevant references. On a more subjective note, do you feel that the results presented are of immediate interest to many people in your own discipline, and/or to people from several disciplines?<br>
&#9679;      Methodology: Please comment on the validity of the approach, quality of the data and quality of presentation. Please note that we expect our reviewers to review all data, including any extended data and supplementary information. Is the reporting of data and methodology sufficiently detailed and transparent to enable reproducing the results?<br>
&#9679;      Comments on statistics: All error bars should be defined in the corresponding figure legends; please comment if that’s not the case. Please include in your report a specific comment on the appropriateness of any statistical tests, and the accuracy of the description of any error bars and probability values.<br>
&#9679;     Conclusions: Do you find that the conclusions and data interpretation are robust, valid and reliable?<br>
&#9679;    Suggested improvements: Please list additional experiments or data that could help strengthening the work in a revision.<br>
&#9679;     References: Does this manuscript reference previous literature appropriately? If not, what references should be included or excluded?<br>

</p>
<h5>Make a recommendation</h5><br>
<p class="middle">Once you’ve read the paper and have assessed its quality, you need to make a recommendation to the editor regarding publication. The specific decision types used by a journal will vary but the key decisions are:<br>

&#9679;    <b>Accept–</b> if the paper is suitable for publication in its current form.<br>
&#9679;    <b>Minor revision–</b> if the paper will be ready for publication after light revisions. Please list the revisions you would recommend the author makes.<br>
&#9679;   <b> Major revision–</b>  if the paper would benefit from substantial changes such as expanded data analysis, widening of the literature review, or rewriting sections of the text.<br>
&#9679;    <b>Reject– </b> if the paper is not suitable for publication with this journal or if the revisions that would need to be undertaken are too fundamental for the submission to continue being considered in its current form.<br>
<br>Reviewers' criticisms are taken seriously; especially, editorial board is very reluctant to disregard technical criticisms. In cases where one reviewer alone opposes publication, consultation with the other reviewers as to whether s/he is applying an unduly critical standard is done. Occasionally suggestions from additional reviewers are taken to resolve a specific issue, for example a specialist technical point, on which we feel a need for further advice.<br>
</p>
<br>
<h5>Sample comments</h5><br>
<p class="middle">Please note that these are just examples of how you might provide feedback on an author’s work. Your review should, of course, always be tailored to the paper in question and the specific requirements of the journal and the editor.</p>
<br>
<h5>&#9632; Positive comments</h5><br>
<p class="middle">
&#9679;     The manuscript is well-written in an engaging and lively style.<br>
&#9679;    The level is appropriate to our readership.<br>
&#9679;     The subject is very important. It is currently something of a “hot topic,” and it is one to which the author(s) have made significant contributions.<br>
&#9679;     This manuscript ticks all the boxes we normally have in mind for an X paper, and I have no hesitation in recommending that it be accepted for publication after a few typos and other minor details have been attended to.<br>
&#9679;     Given the complexity involved, the author has produced a number of positive and welcome outcomes including the literature review which offers a useful overview of current research and policy and the resulting bibliography which provides a very useful resource for current practitioners.<br>
&#9679;    This is a well-written article that does identify an important gap.
</p>
<h5>&#9632; When constructive criticism is required</h5><br>
<p class="middle">
&#9679;  In the “Discussion” section I would have wished to see more information on …<br>
&#9679;   Overall I do not think that this article contains enough robust data to evidence the statement made on page X, lines Y–Z.<br>
&#9679;    I would strongly advise the author(s) of this paper to rewrite their introduction, analysis, and discussion to produce a more contextualized introduction to…<br>
&#9679;    There is an interesting finding in this research about .... However, there is insufficient discussion of exactly what this finding means and what its implications are.<br>
&#9679;    This discussion could be enlarged to explain …<br>
&#9679;   The authors could strengthen the paper by …<br>
&#9679;    The paper would be significantly improved with the addition of more details about …<br>
&#9679;    The abstract is very lengthy and goes into detailed accounts that are best suited for the article’s main discussion sections. As such, it is suggested the section is reduced in size and that only the most important elements remain.<br>
&#9679;    To make this paper publishable the author needs to respond to the following substantive points<br>
</p>
<h5>&#9632; When linguistic alterations are required</h5><br>
<p class="middle">
&#9679;  This paper would benefit from some closer proof reading. It includes numerous linguistic errors (e.g. agreement of verbs) that at times make it difficult to follow. I would suggest that it may be useful to engage a professional English language editor following a restructure of the paper.<br>
&#9679;  The paper is to benefit from making stylistic changes in the way it has been written to make a stronger, clearer, and more compelling argumentative case.<br>
&#9679;   There are a few sentences that require rephrasing for clarity.<br>
</p>
<h5>A note about revisions</h5><br>
<p class="middle">When authors make revisions to their article in response to reviewer comments, they are asked to submit a list of changes and any comments for transmission to the reviewers. The revised version is usually returned to the original reviewer if possible, who is then asked to affirm whether the revisions have been carried out satisfactorily.</p>
<h5>Timing for Review</h5><br>
<p class="middle">Submission to first post-review decision: for manuscripts that are sent to external reviewers, the median time (in days) taken from when a complete submission is received to when an editorial decision post-review is sent to the authors is vary from 10 to 45 days.<br>

Submission to Accept: the median time (in days) from the published submission date to the final editorial acceptance date is vary from 20 to 60 days.</p>
<h5>Article transfer service: If the manuscript is rejected</h5><br>
<p class="middle">IJPS authors can transfer their article submission from one journal to another if they are rejected, without the need to reformat, and without any requirement for further peer review. For this reason, reviewers are informed about the service and are asked for their consent for transferring their review report along with the manuscript to the receiver journal. Reviewers are given the option to reveal their identity to the editor of the receiver journal or stay anonymous. The benefits of manuscript review cascades are twofold:<br>

    &#9679;Reviewers are not asked to review the same manuscript several times for different journals.<br>
    &#9679;Authors do not need to spend additional time reformatting their manuscript.<br>
</p>
