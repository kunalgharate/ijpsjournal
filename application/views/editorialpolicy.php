    <h1><b>Editorial policy</b></h1><br>
    <p>
       The over-riding criteria for publication are originality, high scientific quality and interest to a multidisciplinary audience. Papers not sufficiently substantiated by experimental detail will not be published. Any technical queries will be referred back to the author, although the Editors reserve the right to make alterations in the text without altering the technical content. Manuscripts submitted under multiple authorship are reviewed on the assumption that all listed authors concur with the submission and that a copy of the final manuscript has been approved by all authors and tacitly or explicitly by the responsible authorities in the laboratories where the work was carried out. If accepted, the manuscript shall not be published elsewhere in the same form, in either the same or another language, without the consent of the Editors. Authors must state in a covering letter when submitting papers for publication the novelty embodied in their work or in the approach taken in their research.
