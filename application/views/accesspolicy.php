    <h1><b>Open Access Policy</b></h1>
    <br>
    <p>IJPS provides immediate free access to its content on the principle that making research freely available to the public supports a greater global exchange of knowledge, meaning:</p>
    <br>
    <p>•    Everyone has free and unlimited access to the full-text of articles published in IJPS; manuscripts are freely available without subscription or price barriers</p>
    <br>
    <p>•	Open Access publication is supported by authors' institutes or research funding agency by payment of a comparatively low Article Processing Fee (APF) for accepted articles</p>
    <br>
    <p>•	Open Access publication is more frequently cited due to their high publicity and availability.</p>
   <br>
    <p>•	Articles can be shared and adapted, provided the attribution for the work is given and that the work is not used for commercial purposes.</p>
    <br>
    <p>The open access policy of the IJPS aims at increasing the visibility and accessibility of the published content. Online free access and listing and indexing in a larger number of bibliographic databases add to the high visibility and research impact.</p>
